/**
 * Parses a text string and replaces [EmojiName] with actual Unicode Emojis.
 * @param {string} text - The text to parse.
 * @returns {string} - The parsed text.
 */
export function parseWeChatEmojis(text) {
    if (!text || typeof text !== 'string') return text;

    // Regular expression to find [EmojiName]
    // We use a non-greedy match.
    return text.replace(/\[([^\]]+)\]/g, (match, name) => {
        const emoji = WECHAT_EMOJI_MAP[name];
        if (emoji) {
            return emoji;
        }
        return match; // Return original if no mapping found
    });
}
